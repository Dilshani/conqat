package org.conqat.engine.dotnet.ila.xml;

/**
 * Enumeration of the xml attributes the IL-XML format comprises.
 * 
 * @author Elmar Juergens
 * @author $Author: juergens $
 * 
 * @version $Revision: 44714 $
 * @ConQAT.Rating GREEN Hash: F4EE0F3BE4B8636CD6D1D3071BC8CED8
 */
public enum EIlaXmlAttribute {

	/** Name of a type */
	Type,

	/** Name of a member */
	Name,

	/** Visibility of a member */
	Visibility,

	/** Metadata token of a member */
	Token,

	/** Type of a member (i.e. constructor, method, ...) */
	MemberType,

	/** Number of IL statements of the implementation of the member's body. */
	NumILStmts,

	/**
	 * Indicates that code unit has been generated by the compiler and has no
	 * counterpart in the source code.
	 */
	Synthetic,

	/** Indicates whether a type is a class, interface, struct, ... */
	DeclType,

	/** Indicates whether type is abstract */
	IsAbstract,

	/** Description of assembly */
	Assembly,

	/** IL Statements of method body */
	// TODO (EJ) Rename
	BodyZip,
}